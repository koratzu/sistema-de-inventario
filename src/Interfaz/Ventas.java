    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
       
        Metodos_Ventas DatosVentas = new Metodos_Ventas(txtPagoCon.getText(),txtArticuloVentas.getText(),
             Integer.parseInt(txtCantidadArticulos.getText()),Double.parseDouble(txtPrecioArticulos.getText()));
        
        DefaultTableModel ModeloVentas = (DefaultTableModel) TablaIngresarVentas.getModel();
        Object[] filaVentas =  new Object[6];
        
        filaVentas [0] = DatosVentas.getPago_con();
        filaVentas [1] = DatosVentas.getArticulo();
        filaVentas [2] = DatosVentas.getCantidadArticulo();
        filaVentas [3] = DatosVentas.getPrecioArticulo();
        filaVentas [5] = String.valueOf(DatosVentas.getPrecioArticulo()*DatosVentas.getCantidadArticulo());
        ModeloVentas.addRow(filaVentas);
        
    
    }//GEN-LAST:event_btnAgregarActionPerformed
    
        
   
