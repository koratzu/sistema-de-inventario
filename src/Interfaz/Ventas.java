    private void btnAgregarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarClienteActionPerformed
     Metodos_Clientes DatosClientes = new Metodos_Clientes(txtNombreCompleto.getText(), txtApellidos.getText(), Integer.parseInt(txtIDCliente.getText()),
                                                     txtTelefonos.getText(), txtPagoDeudas.getText());
        
     DefaultTableModel ModeloVentas = (DefaultTableModel) TablaIngresarClientes.getModel();
        Object [] filaClientes =  new Object[5];
        filaClientes [0] = DatosClientes.getIdCliente();
        filaClientes [1] = DatosClientes.getNombre();
        filaClientes [2] = DatosClientes.getApellido();
        filaClientes [3] = DatosClientes.getTelefono();
        filaClientes [4] = DatosClientes.getPrecio();
        ModeloVentas.addRow(filaClientes);
        
                                          
        
    }//GEN-LAST:event_btnAgregarClienteActionPerformed

    private void btnModificarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarClienteActionPerformed
        if (TablaIngresarClientes.getSelectedRow()>=0)
        {
            int filaClientes = TablaIngresarClientes.getSelectedRow();
            TablaIngresarClientes.setValueAt(txtIDCliente.getText(), filaClientes, 0);
            TablaIngresarClientes.setValueAt(txtNombreCompleto.getText(), filaClientes, 1);
            TablaIngresarClientes.setValueAt(txtApellidos.getText(), filaClientes, 2);
            TablaIngresarClientes.setValueAt(txtTelefonos.getText(), filaClientes, 3);
            TablaIngresarClientes.setValueAt(txtPagoDeudas.getText(), filaClientes, 4);
        }
    }//GEN-LAST:event_btnModificarClienteActionPerformed

