    private void btnEliminarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarClienteActionPerformed
    DefaultTableModel ModeloVentas = (DefaultTableModel) TablaIngresarClientes.getModel();
    int fila =  TablaIngresarClientes.getSelectedRow();
    if(fila<0)
    {
        JOptionPane.showMessageDialog(null, "Debe seleccionar una fila de la tabla para eliminarla");
    }
    else
    {
        int respuesta = JOptionPane.showConfirmDialog(null,"Esta seguro?","Alerta",JOptionPane.YES_NO_OPTION);
        if (JOptionPane.OK_OPTION == respuesta) {
            ModeloVentas.removeRow(fila);
        }
        
    }
    }//GEN-LAST:event_btnEliminarClienteActionPerformed

